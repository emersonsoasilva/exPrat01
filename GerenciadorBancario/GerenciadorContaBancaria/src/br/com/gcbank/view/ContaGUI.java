    private void AgruparFaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgruparFaixaActionPerformed
        Map<String, List<ContaCorrente>> agruparFaixa = contaService.getContas().stream()
                .collect(groupingBy(
                        conta -> {
                            if (conta.getSaldo() <= 5000) return "Até R$5000,00";
                            else if (conta.getSaldo() <= 10000 ) return "De R$5001 até R%10000";
                            else return "Acima de R$10000";
                        }
                ));
       agruparFaixa.forEach((faixa, lista) -> {
            txtAreaArquivo.append(faixa + ":\n");
            lista.forEach(c -> txtAreaArquivo.append(
                String.format("  Conta %d | %s | R$ %.2f \n\n", c.getNumero(), c.getTitular(), c.getSaldo())
            ));
        });
    }//GEN-LAST:event_AgruparFaixaActionPerformed

    private void SaldoMaiorPredicateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaldoMaiorPredicateActionPerformed
        txtAreaArquivo.append("\nContas com o saldo superior a R$5.000 e com número de conta Par:\n\n");
        
        Predicate<Conta> salarioMaior = s -> s.getSaldo() > 5000 && s.getNumero() % 2 == 0;
        
        List<Conta> filtrados = contaService.getContas().stream()
                .filter(salarioMaior)
                .collect(Collectors.toList());
        
        filtrados.forEach((conta) -> {
            txtAreaArquivo.append(conta.getNumero() + " " + conta.getTitular() + " " + conta.getSaldo());
        });
        
    }//GEN-LAST:event_SaldoMaiorPredicateActionPerformed

    private void OrdenarNomesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrdenarNomesActionPerformed
        // TODO add your handling code here:
       Comparator<Conta> ordenacaoNomes = (t1, t2) -> t1.getTitular().compareTo(t2.getTitular());
       
       List<Conta> nomesOrdenados = contaService.getContas().stream()
               .sorted(ordenacaoNomes)
               .collect(Collectors.toList());
       
        
        txtAreaArquivo.append("\nNomes Ordenados em Ordem alfabética:\n\n");
        
        nomesOrdenados.forEach((conta) -> {
            txtAreaArquivo.append(conta.getNumero() + " " + conta.getTitular() + " " + conta.getSaldo() + "\n");
        });
    }//GEN-LAST:event_OrdenarNomesActionPerformed

